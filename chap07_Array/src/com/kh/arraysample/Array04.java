package com.kh.arraysample;

public class Array04 {

	public static void main(String[] args) {
		// Array 예제
		//사과에는 바나나, 딸기에는 수박, 키위에는 레몬, 메론에는 귤, 귤에는 오렌지
		String[] fruitArray = {"사과","딸기","키위","메론","귤", };
		fruitArray[0] = "바나나"; 
		fruitArray[1] = "수박";	
		fruitArray[2] = "레몬";
		fruitArray[3] = "귤";
		fruitArray[4] = "오렌지";
		System.out.println(fruitArray[0]+fruitArray[1]);
	
	
	}

}
